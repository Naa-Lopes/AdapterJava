package adapter;

public class RoundPeg {
    private double radius;

    // Default constructor
    public RoundPeg() {}

    // Constructor to initialize the radius of the round peg
    public RoundPeg(double radius) {
        this.radius = radius;
    }

    // Getter for the radius of the round peg
    public double getRadius() {
        return radius;
    }
}
